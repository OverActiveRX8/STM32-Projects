/**
  * @brief This function handles TIM6 global interrupt, DAC1 and DAC2 underrun error interrupts.
  */
void TIM6_DAC_IRQHandler(void)
{
  /* USER CODE BEGIN TIM6_DAC_IRQn 0 */

  /* USER CODE END TIM6_DAC_IRQn 0 */
  HAL_TIM_IRQHandler(&htim6);
  /* USER CODE BEGIN TIM6_DAC_IRQn 1 */
	#ifdef USE_VREFINT
	vref = getadcValue(&adc1, 2, 1, 8);
	temp = getadcValue(&adc1, 2, 0, 8) * vrefint / vref;
	temp = (temp - 0.76f) / 0.0025f + 25.0f;
	vdc = getadcValue(&adc2, 2, 0, 12) * vrefint / vref;
	vac = getadcValue(&adc2, 2, 1, 12) * vrefint / vref;
	#else
	temp = getadcValue(&adc1, 2, 0, 8);
	temp = (temp - 0.76f) / 0.0025f + 25.0f;
	vdc = getadcValue(&adc2, 2, 0, 12);
	vac = getadcValue(&adc2, 2, 1, 12);
	#endif
	vacAvg = CalcAVG(vac, avgBuffer);
	vacRms = CalcRMS(vac, rmsBuffer);
  /* USER CODE END TIM6_DAC_IRQn 1 */
}

